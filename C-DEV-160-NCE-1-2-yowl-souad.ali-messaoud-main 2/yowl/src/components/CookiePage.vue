Personnalisation des publicités pour Android
Désactiver cette fonctionnalité via les paramètres de votre appareil (les instructions précises varient selon la version). 
Permettre aux apps de demander à faire le suivi pour les versions 14 et ultérieures d'iOS 
Désactivez cette fonctionnalité par le biais des paramètres de votre appareil (les instructions précises varient selon la version). 
Personnaliser en fonction de votre paramètre d'identité déduite dans YOWL
