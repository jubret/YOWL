Quelles sont mes options en matière de confidentialité ?
 <br />
Nous nous engageons à vous offrir un certain nombre d'options de confidentialité dédiées au contrôle ou à la restriction de l'utilisation que nous, nos partenaires ou autres tierces parties faisons des cookies et données relatives aux cookies :
<br />
YOWL stocke des informations sur les autres sites Web où vous avez vu du contenu YOWL ou accédé à nos services. Pour la plupart des fonctionnalités de YOWL, vous pouvez ajuster cela en allant dans : Paramètres et confidentialité > Confidentialité et sécurité > Activité hors YOWL > Autoriser l’utilisation aux endroits où vous voyez du contenu YOWL sur le Web. Toutefois, pour certaines fonctionnalités d'abonnement payantes, même si vous effectuez ce réglage, nous continuerons à stocker certaines informations lorsque cela est nécessaire pour que la fonctionnalité en question soit opérationnelle. Nous vous informerons de ce stockage lorsque vous vous inscrirez à ces fonctionnalités.  
<br />
Si cette option est désactivée ou si vous vous trouvez dans l'Union européenne ou en Islande, Liechtenstein, Norvège ou Suisse, nous ne stockerons pas ni n’utiliserons ces visites de pages Web pour améliorer votre expérience à l'avenir. Cependant, si nous avions préalablement stocké votre historique de navigation sur le Web, nous pourrons continuer à personnaliser votre expérience en fonction des informations que nous avons déjà apprises de cet historique.
